#include <play-man/gameboy/cpu/CpuCore.hpp>

namespace GameBoy
{
    uint8_t	CpuCore::GetInterruptRegister()
    {
        return (IE);
    }

    void CpuCore::SetFlag(FlagRegisterFlag flag, bool enable)
    {
        if (enable)
        {
            AF.SetHighByte(AF.HighByte() | static_cast<uint8_t>(flag));
        }
        else
        {
            AF.SetHighByte(AF.HighByte() & ~(static_cast<uint8_t>(flag)));
        }
    }
} /* namespace Gameboy */
